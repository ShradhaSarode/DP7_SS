package com.StringAssignment;
/*
 7.Write a Java program to convert lowercase string to uppercase.
 */
public class SeventhQ
{
	public static void main(String[] args)
	{
		String s="ritu";
		System.out.println("lowercase string to uppercase:"+s.toUpperCase());
		String s1="SHRADHA";
		System.out.println("uppercase string to lowercase:"+s1.toLowerCase());
	}

}
