//WAP to print squares from 1 to20
package com.loopassign2;

//import java.util.Scanner;

public class Square_of_num
{
	public static void main(String[] args)
	{
		 for (int i = 1; i <= 20; i++)
		 {
	        int square = i * i;
	        System.out.println("Square of " + i + " is: " + square);
	     }
	}
}
