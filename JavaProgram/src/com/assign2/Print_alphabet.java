//5.Write a Java program to print all alphabets from a to z. - using  for loop
package com.assign2;

public class Print_alphabet
{

	public static void main(String[] args)
	{
		System.out.println("Alphabets from a to z:");
		
		for(char i='a';i<='z';i++)
		{
			System.out.println(i);
		}

	}

}
